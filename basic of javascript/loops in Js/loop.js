//write a different types of loops in JS//

//1. for loop//
//write a program to print 10 natural numbers//
let num;
console.log("using for loop");
console.log("TEN NATURAL NUMBERS = ");
for(num=1;num<=10;num++)
{
    console.log(num);
}

//2.while loop//
let n=1;
console.log("using while loop");
console.log("TEN NATURAL NUMBERS = ");
while(n<=10)
{
    console.log(n);
    n++;
}

//3. do...while loop//

let x=1;
console.log("using do while loop");
console.log("TEN NATURAL NUMBERS = ");
do{
    console.log(x);
    x++;
}while(x<=10);